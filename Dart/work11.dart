void main() {
  var t = 'A';
  if (t == 'E') {
    print("You have Chosen: Excelent");
  } else if (t == 'V') {
    print("You have Chosen: Very Good");
  } else if (t == 'A') {
    print("You have Chosen: Average");
  } else if (t == 'G') {
    print("You have Chosen: Good");
  } else if (t == 'F') {
    print("You have Chosen: Fail");
  } else {
    print("You have chosen invalid Grade");
  }
}
